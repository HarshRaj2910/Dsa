package loop;
import java.util.Scanner;
public class f {
    public static void main(String[] args) {
        // print number from 1 to 10
        for(int i=0;i<=10;i++){
            System.out.println(i);
    
        }

        // print number 1 to n
        Scanner sc=new Scanner(System.in);
        int n=sc.nextInt();
        for(int j=1;j<=n;j++ ){
            System.out.println(j);
        }

    }
    
    
}
